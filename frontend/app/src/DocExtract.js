              <button className="btn-ghost" onClick={() => { setIsEditing(true); setActiveTab("datapoints"); }} style={{ padding: '8px 16px', borderRadius: 8, border: '1px solid var(--border)', background: 'var(--surface)' }}>Add Data Points</button>
